package day02;

public class Chengfabiao {

	public static void main(String[] args) {
		//打印乘法表
		for(int i=1;i<=9;i++){
			for(int j=1;j<=i;j++){
				System.out.print(i+"*"+j+"="+i*j+"\t");
			}
		System.out.println();
		}
		//打印三角
		for(int i=1;i<=5;i++){
			for(int j=1;j<=i;j++){
				System.out.print("*"+"\t");
			}
			System.out.println();
		}
		//*
		//**
		//***
		//****


	}

}
